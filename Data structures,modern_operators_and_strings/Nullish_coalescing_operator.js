//NULLISH COALESCING OPERATOR ??

/*

THIS TREATS O AND ' ' AS TRUTHY VALUE
SO THE NULISH OPERATOR WORKS WITH NULISH VALUES (NULL AND UNDEFINED) AND NOT FALSEY VALUES (0 AND 1)
Above when we used || operator it sees 0 as falsy and we might want to use 0 as a value but || will assume 0 is falsey so the nullish operator ?? is a solution to that

*/

const guestCorrect = restaurant.numGuests ?? 10;
console.log(guestCorrect);
